package modelo;

// Se creó esta clase para representar las distintas categorías o modos de juego disponibles en el juego 

public enum CategoriaJuego {
    CLASICO,           // Modo clásico del juego PacMan, sin power-ups ni mejoras adicionales.
    CLASICO_MEJORADO,  //  Versión mejorada que incluye power-ups (como velocidad, invencibilidad, congelar fantasmas, etc.).
    MULTIJUGADOR       //  (Planeado o futuro) Modo multijugador donde posiblemente jueguen varios usuarios simultáneamente.
}
